uint8_t hexDigit(char c)
{
    return hexdigits[(uint8_t)c];
}

std::vector<uint8_t> FromHex(const char* charHex)
{
    // convert hex to uint8_t vector
    std::vector<uint8_t> vch;
    while (true) {
        while (isspace(*charHex))
            charHex++;
        uint8_t c = hexDigit(*charHex++);
        if (c == (uint8_t)-1)
            break;
        uint8_t n = (c << 4);
        c = hexDigit(*charHex++);
        if (c == (uint8_t)-1)
            break;
        n |= c;
        vch.push_back(n);
    }
    return vch;
}

std::vector<uint8_t> FromHex(const std::string& strHex)
{
    return FromHex(strHex.c_str());
}